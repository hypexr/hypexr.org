	<h1>Example syntax for Secure Copy (<b>scp</b>)</h1>
	<h3>What is Secure Copy?</h3>
	<p><b>scp</b> allows files to be copied to, from, or between different hosts.  It uses <b>ssh</b> for data transfer and provides the same authentication and same level of security as <b>ssh</b>.</p>
	<h3>Examples</h3>

	<h4>Copy the file "foobar.txt" from a remote host to the local host</h4>
	<ul><table class="examples" border="0" cellspacing="0" cellpadding="10"><tr><td>
	    $ scp your_username@remotehost.edu:foobar.txt /some/local/directory
	</td></tr></table></ul>
	<h4>Copy the file "foobar.txt" from the local host to a remote host</h4>
	<ul><table class="examples" border="0" cellspacing="0" cellpadding="10"><tr><td>
	    $ scp foobar.txt your_username@remotehost.edu:/some/remote/directory
	</td></tr></table></ul>
	<h4>Copy the directory "foo" from the local host to a remote host's directory "bar"</h4>
	<ul><table class="examples" border="0" cellspacing="0" cellpadding="10"><tr><td>
	    $ scp -r foo your_username@remotehost.edu:/some/remote/directory/bar
	</td></tr></table></ul>
	<h4>Copy the file "foobar.txt" from remote host "rh1.edu" to remote host "rh2.edu"</h4>
	<ul><table class="examples" border="0" cellspacing="0" cellpadding="10"><tr><td>
	    $ scp your_username@rh1.edu:/some/remote/directory/foobar.txt \<br>your_username@rh2.edu:/some/remote/directory/
	</td></tr></table></ul>
	<h4>Copying the files "foo.txt" and "bar.txt" from the local host to your home directory on the remote host</h4>
	<ul><table class="examples" border="0" cellspacing="0" cellpadding="10"><tr><td>
	    $ scp foo.txt bar.txt your_username@remotehost.edu:~
	</td></tr></table></ul>
	<h4>Copy the file "foobar.txt" from the local host to a remote host using port 2264</h4>
	<ul><table class="examples" border="0" cellspacing="0" cellpadding="10"><tr><td>
	    $ scp -P 2264 foobar.txt your_username@remotehost.edu:/some/remote/directory
	</td></tr></table></ul>
	<h4>Copy multiple files from the remote host to your current directory on the local host</h4>
	<ul><table class="examples" border="0" cellspacing="0" cellpadding="10"><tr><td>
	    $ scp your_username@remotehost.edu:/some/remote/directory/\{a,b,c\} .
	</td></tr></table></ul>
	<ul><table class="examples" border="0" cellspacing="0" cellpadding="10"><tr><td>
	    $ scp your_username@remotehost.edu:~/\{foo.txt,bar.txt\} .
	</td></tr></table></ul>
	<h3><b>scp</b> Performance</h3>
	<p>By default <b>scp</b> uses the Triple-DES cipher to encrypt the data being sent.  Using the Blowfish cipher has been shown to increase speed.  This can be done by using option <i>-c blowfish</i> in the command line.</p>
	<ul><table class="examples" border="0" cellspacing="0" cellpadding="10"><tr><td>
	    $ scp -c blowfish some_file your_username@remotehost.edu:~
	</td></tr></table></ul>
	<p>It is often suggested that the <i>-C</i> option for compression should also be used to increase speed.  The effect of compression, however, will only significantly increase  speed if your connection is very slow.  Otherwise it may just be adding extra burden to the CPU.  An example of using blowfish and compression:
	<ul><table class="examples" border="0" cellspacing="0" cellpadding="10"><tr><td>
	    $ scp -c blowfish -C local_file your_username@remotehost.edu:~
	</td></tr></table></ul>
